package pojo.hakduck_commodity;


public class CommodityKey {

  private long id;
  private String dityName;
  private String dityCode;
  private long priceTime;
  private long weight;
  private double price;
  private long dityStatus;
  private String dityDescribe;
  private long brandId;
  private long commodityCommentid;
  private long commoditySpecificationsId;
  private java.sql.Date entryDate;
  private java.sql.Date modifiedTime;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getDityName() {
    return dityName;
  }

  public void setDityName(String dityName) {
    this.dityName = dityName;
  }


  public String getDityCode() {
    return dityCode;
  }

  public void setDityCode(String dityCode) {
    this.dityCode = dityCode;
  }


  public long getPriceTime() {
    return priceTime;
  }

  public void setPriceTime(long priceTime) {
    this.priceTime = priceTime;
  }


  public long getWeight() {
    return weight;
  }

  public void setWeight(long weight) {
    this.weight = weight;
  }


  public double getPrice() {
    return price;
  }

  public void setPrice(double price) {
    this.price = price;
  }


  public long getDityStatus() {
    return dityStatus;
  }

  public void setDityStatus(long dityStatus) {
    this.dityStatus = dityStatus;
  }


  public String getDityDescribe() {
    return dityDescribe;
  }

  public void setDityDescribe(String dityDescribe) {
    this.dityDescribe = dityDescribe;
  }


  public long getBrandId() {
    return brandId;
  }

  public void setBrandId(long brandId) {
    this.brandId = brandId;
  }


  public long getCommodityCommentid() {
    return commodityCommentid;
  }

  public void setCommodityCommentid(long commodityCommentid) {
    this.commodityCommentid = commodityCommentid;
  }


  public long getCommoditySpecificationsId() {
    return commoditySpecificationsId;
  }

  public void setCommoditySpecificationsId(long commoditySpecificationsId) {
    this.commoditySpecificationsId = commoditySpecificationsId;
  }


  public java.sql.Date getEntryDate() {
    return entryDate;
  }

  public void setEntryDate(java.sql.Date entryDate) {
    this.entryDate = entryDate;
  }


  public java.sql.Date getModifiedTime() {
    return modifiedTime;
  }

  public void setModifiedTime(java.sql.Date modifiedTime) {
    this.modifiedTime = modifiedTime;
  }

}
